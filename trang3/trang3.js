function openSide() {
    document.getElementById("my-side-qlct").style.width = "30vh";
    document.getElementById("main-my-side-qlct").style.marginLeft = "30vh";
}

function closeSide() {
    document.getElementById("my-side-qlct").style.width = "0";
    document.getElementById("main-my-side-qlct").style.marginLeft= "0";
}
